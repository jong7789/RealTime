// ---- Initialize 10G PHY -----------------------------------------------------
//
void ael2005_init()
{
    int i;
    int n = sizeof(phy_mcu_code) / sizeof(phy_mcu_code[0]);

    // AEL2005 PHY reset
//  phy_hw_reset();                         // PHY hardware reset (keep RESET_N low for 100 ms)
//  usleep(100000);                         // Wait for 100 ms for device to get out of reset
    mdio_write(0x01, 0x0000, 0xA040);       // PHY software reset
    usleep(100000);                         // Wait for 100 ms for device to get out of reset

    // Platform-specific MDIO patches
    mdio_write(0x01, 0xC017, 0xFEB0);       // Flip RX_LOS polarity
    mdio_write(0x01, 0xC01F, 0x8728);       // Flip HSRXDATA polarity
    mdio_write(0x01, 0xC013, 0xF341);       // Invert lxmit clock
    mdio_write(0x01, 0xC210, 0x8000);       // Reset datapath
    mdio_write(0x01, 0xC210, 0x8100);       // Reset datapath
    mdio_write(0x01, 0xC210, 0x8000);       // Reset datapath
    mdio_write(0x01, 0xC210, 0x0000);       // Reset datapath
    usleep(50000);                          // Wait for 50 ms for datapath reset to complete

    // Transceiver-specific MDIO patches
    mdio_write(0x01, 0xC003, 0x0181);       // Bit 7: enable the CDR inc setting in 1.C005
    mdio_write(0x01, 0xC010, 0x448A);       // Bit 14: mask out high BER input from the LOS signal in 1.000A

    // Transceiver-specific microcontroller initialization
    mdio_write(0x01, 0xC04A, 0x5200);       // Activate microcontroller and pause
    usleep(50000);                          // Wait 50 ms for microcontroller before writing in code
    for (i = 0; i < n; i++)                 // Write microcontroller code to the PHY
        mdio_write(phy_mcu_code[i].reg,
                   phy_mcu_code[i].addr,
                   phy_mcu_code[i].val);
    mdio_write(0x01, 0xCA00, 0x0080);       // Unpause the microcontroller
    mdio_write(0x01, 0xCA12, 0x0000);       // to start the program
    usleep(100000);                         // Wait for 100 ms

    return;
}
